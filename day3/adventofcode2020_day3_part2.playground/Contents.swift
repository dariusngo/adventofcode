struct Path {
    let right: Int
    let down: Int
}

extension StringProtocol {
    subscript(offset: Int) -> Character? {
        self[index(startIndex, offsetBy: offset)]
    }
}

let lines = input.components(separatedBy: "\n")

let paths: [Path] = [
    Path(right: 1, down: 1),
    Path(right: 3, down: 1),
    Path(right: 5, down: 1),
    Path(right: 7, down: 1),
    Path(right: 1, down: 2),
]

func getTreesHitOnSlope(path: Path, slope: [String]) -> Int {
    var xPosition = 0
    var yPosition = 0
    var treesHit = 0

    while yPosition < slope.count {
        if slope[yPosition][xPosition] == "#" {
            treesHit += 1
        }

        xPosition += path.right
        
        if slope[yPosition].count <= xPosition {
            xPosition -= slope[yPosition].count
        }
        
        yPosition += path.down
        
    }
    
    return treesHit
}

func getTotalTreesHit(paths: [Path], slope: [String]) -> Int {
    paths.reduce(1) { result, path -> Int in
        return result * getTreesHitOnSlope(path: path, slope: slope)
    }
}

print(getTotalTreesHit(paths: paths, slope: lines))
